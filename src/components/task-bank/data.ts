// Function to flatten topic structure for use in filters
export const getAllTopics = () => {
  return topicStructure.map(topic => topic.name);
};

export const getAllSubtopics = () => {
  const subtopics: { id: string; name: string; parentTopic: string }[] = [];
  
  topicStructure.forEach(topic => {
    topic.subtopics.forEach(subtopic => {
      subtopics.push({
        id: subtopic.id,
        name: subtopic.name,
        parentTopic: topic.name
      });
    });
  });
  
  return subtopics;
};

